#include "MousePointer.h"


/******************************************************************************
**	グローバル変数
******************************************************************************/


/******************************************************************************
** 関数名: GetMousePointer
** 関数の概要
** 引数  : HWND hWnd
** 戻り値: マウス座標 POINT
** 説明  :
******************************************************************************/
POINT GetMousePointer( HWND hWnd )
{
	// マウスの座標を取得する
	POINT po;
	GetCursorPos( &po );
	ScreenToClient( hWnd, &po );

	// マウス座標を返す
	return po;

}


/******************************************************************************
** 関数名: GetMousePointer
** 関数の概要
** 引数  : HWND hWnd
** 戻り値: マウス座標 POINT
** 説明  :
******************************************************************************/
void GetMousePointer( HWND hWnd, POINT *po )
{
	// マウスの座標を取得する
	GetCursorPos( po );
	ScreenToClient( hWnd, po );

}

